        // Somme sécurisée des APCPsfc horaires (lecture via reflection, parse selon type)
        private double SumPrecipitation(HourlyData hd)
        {
            if (hd == null) return 0;
            double total = 0;
            var props = hd.GetType().GetProperties();
            foreach (var p in props)
            {
                var hourObj = p.GetValue(hd);
                if (hourObj == null) continue;
                var apcpProp = hourObj.GetType().GetProperty("APCPsfc");
                if (apcpProp == null) continue;
                var val = apcpProp.GetValue(hourObj);
                if (val == null) continue;

                double parsed = 0;
                if (val is double dv) parsed = dv;
                else if (val is float fv) parsed = fv;
                else if (val is int iv) parsed = iv;
                else if (val is long lv) parsed = lv;
                else if (val is decimal decv) parsed = (double)decv;
                else
                {
                    var s = val.ToString();
                    double.TryParse(s, NumberStyles.Any, CultureInfo.InvariantCulture, out parsed);
                }

                total += parsed;
            }
            return total;
        }

        // Calcul simple du "ressenti" : moyenne des TMP2m horaires si existants sinon (tmax + tmin)/2
        private double ComputeRessenti(HourlyData hd, int? tmax, int? tmin)
        {
            if (hd != null)
            {
                var props = hd.GetType().GetProperties();
                double sum = 0;
                int count = 0;
                foreach (var p in props)
                {
                    var hourObj = p.GetValue(hd);
                    if (hourObj == null) continue;
                    var tmpProp = hourObj.GetType().GetProperty("TMP2m");
                    if (tmpProp == null) continue;
                    var val = tmpProp.GetValue(hourObj);
                    if (val == null) continue;

                    double parsed = 0;
                    if (val is double dv) parsed = dv;
                    else if (val is float fv) parsed = fv;
                    else if (val is int iv) parsed = iv;
                    else if (val is long lv) parsed = lv;
                    else
                    {
                        double.TryParse(val.ToString(), NumberStyles.Any, CultureInfo.InvariantCulture, out parsed);
                    }

                    sum += parsed;
                    count++;
                }

                if (count > 0)
                {
                    return sum / count;
                }
            }

            // fallback : moyenne tmax/tmin
            if (tmax.HasValue && tmin.HasValue)
            {
                return (tmax.Value + tmin.Value) / 2.0;
            }

            return 0;
        }
